    //I: Segregation des interfaces
        //Une classe ne doit implementer que des methodes dont elle a besoin
    //D: Inversion des dependances

    //Design Patern
        //-Singleton( Une classe doit instanciee une seule fois)
        //-Factory(Permet d'instancier les classes)
